// Calculate derived metrics
export const getDerivedMetrics = () => {
  const criticalCount = legacySystems.filter(s => s.status === "critical").length;
  const warningCount = legacySystems.filter(s => s.status === "warning").length;
  const onTrackCount = legacySystems.filter(s => s.status === "on-track").length;

  const today = new Date();
  const goLive = new Date(projectMetadata.goLiveDate);
  const daysToGoLive = Math.ceil((goLive - today) / (1000 * 60 * 60 * 24));

  return {
    criticalCount,
    warningCount,
    onTrackCount,
    daysToGoLive,
    budgetPercentage: ((projectMetadata.budgetConsumed / projectMetadata.totalBudget) * 100).toFixed(1),
  };
};

